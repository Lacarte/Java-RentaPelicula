public class ObjPeliculaCopelNumcopia {

    private int codpel;
    private int numcopia;

    public ObjPeliculaCopelNumcopia(int codpel, int numcopia) {
        this.codpel = codpel;
        this.numcopia = numcopia;
    }

    public int getCodpel() {
        return codpel;
    }

    public int getNumcopia() {
        return numcopia;
    }

}
